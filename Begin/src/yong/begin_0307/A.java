package yong.begin_0307;

public class A {

	protected String name;
	protected int salary;

	public A() {

	}

	public A(String name, int salary) {

		super();
		this.name = name;
		this.salary = salary;
	}

	public String getName() {
		return name;
	}

	public int getSalary() {
		return salary;
	}

}
